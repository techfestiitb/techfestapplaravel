                                    <h3 class="text-black h4 mb-4">What is Model United Nations?</h3>
                                    <p>Model United Nations (also Model UN or MUN) is an academic simulation of the United Nations that aims to educate and encourage participants to discuss about major issues concerning the world, topics in international relations, diplomacy and the United Nations agenda.</p>
                                </div>

                                <div class="mb-5" data-aos="fade-up" data-aos-delay="100">
                                    <h3 class="text-black h4 mb-4">What are the dates of Techfest World MUN?</h3>
                                    <p>Techfest World MUN, IIT Bombay is a three day conference and will be held during Techfest 2019-20 from 3rd-5th January 2020.</p>
                                </div>

                                <div class="mb-5" data-aos="fade-up" data-aos-delay="100">
                                    <h3 class="text-black h4 mb-4"> What awards will be provided at TWMUN 2019?</h3>
                                    <p>The awards will be presented at the discretion of the chairs of each committee and will include 1 Best Delegate and 1 Honorable Mentions/High Commendations. A Best Delegation award will also be provided, and 1 award to the best International Ambassador at the discretion of the Secretary General and the Organizing committee.</p>
                                </div>

                                <div class="mb-5" data-aos="fade-up" data-aos-delay="100">
                                    <h3 class="text-black h4 mb-4">Who can register for TWMUN, IIT Bombay?</h3>
                                    <p>All students with a valid ID card of their high school or college can register for TWMUN, IIT Bombay. However, exceptional cases are sometimes made for recent graduates.</p>
                                </div>


                            </div>
                            <div class="col-lg-6">

