                                    <h3 class="text-black h4 mb-4">When will the study guides be released?</h3>
                                    <p>The Study guides will be released prior to the allotments before start of October.</p>
                                </div>


                                <div class="mb-5" data-aos="fade-up" data-aos-delay="100">
                                    <h3 class="text-black h4 mb-4">Why are there two topics listed for each committee?</h3>
                                    <p>Delegates will spend the first session deciding and voting on which topic to discuss, and will debate that issue first. If that topic is resolved before the end of the conference, the second issue will be debated too, but this cannot be guaranteed. Delegates should prepare equally well for both topics.</p>
                                </div>

                                <div class="mb-5" data-aos="fade-up" data-aos-delay="100">
                                    <h3 class="text-black h4 mb-4">What is the best way to reach the venue?</h3>
