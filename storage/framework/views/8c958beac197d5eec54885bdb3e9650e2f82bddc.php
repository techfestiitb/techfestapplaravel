                                </div>

                                <div class="mb-5" data-aos="fade-up" data-aos-delay="100">
                                    <h3 class="text-black h4 mb-4">Do I get help for my VISA application?</h3>
                                    <p>If you need assistance with your Visa application, please email us a scanned copy of your passport. Once your payment of Delegate fees is complete, TWMUN, IIT Bombay will send you an invitation letter to the conference, which should then allow you to apply for a Visa. Thus, we recommend that you register to the conference and notify us that you require a Visa as early as possible.</p>
